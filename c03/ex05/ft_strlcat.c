#include <unistd.h>
#include <stdio.h>

unsigned int	ft_strlcat(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (dest[i] && i < size)
		i++;
	while (src[j] && (i + j + 1) < size)
	{
		dest[i + j] = src[j];
		j++;
	}
	if (i < size)
		dest[i + j] = '\0';
	while (src[j])
		j++;
	return (i + j);
}

int main(void)
{
    char dest[20] = "Hello, ";
    char src[] = "world!";
    unsigned int result = ft_strlcat(dest, src, sizeof(dest));

    printf("%s\n", dest);       
    printf("%u\n", result);     

    return 0;
}

